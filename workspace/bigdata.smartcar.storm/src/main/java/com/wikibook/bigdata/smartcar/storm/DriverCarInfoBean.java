package com.wikibook.bigdata.smartcar.storm;

import org.apache.log4j.Logger;
 
public class DriverCarInfoBean {
	
	String date;
	String carNumber;
	String speedPedal;
	String breakPedal;
	String steerAngle;
	String directLight;
	int speed;
	String areaNumber;

	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	
	
	public String getSpeedPedal() {
		return speedPedal;
	}
	public void setSpeedPedal(String speedPedal) {
		this.speedPedal = speedPedal;
	}
	
	public String getBreakPedal() {
		return breakPedal;
	}
	public void setBreakPedal(String breakPedal) {
		this.breakPedal = breakPedal;
	}
	
	
	public String getSteerAngle() {
		return steerAngle;
	}
	public void setSteerAngle(String steerAngle) {
		this.steerAngle = steerAngle;
	}
	
	
	public String getDirectLight() {
		return directLight;
	}
	public void setDirectLight(String directLight) {
		this.directLight = directLight;
	}
	
	
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	
	
	public String getAreaNumber() {
		return areaNumber;
	}
	public void setAreaNumber(String areaNumber) {
		this.areaNumber = areaNumber;
	}
	
 

 
	 
}